#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;

    // N^2 Complexity 


    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= n; j++)
        {
            cout << "hello" << endl;
        }
    }


for (int i = 0; i <= n-1; i++)
{
    for (int j = i+1; j <n ; j++)
    {
         cout << "Hi" << endl;
    }
}
    cout << "";

    return 0;
}